int mul[5] = {0, 1, 14, 36, 24};

int conb(int n, int k) {
  int ans = 1;
  for (int i = 0; i < k; ++i) {
    ans *= (n - i);
  }
  for (int i = 1; i <= k; ++i) {
    ans /= i;
  }
  return ans;
}

