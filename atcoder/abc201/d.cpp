string s[2005];
int dp[2005][2005];

void solve() {
  int h, w;
  cin >> h >> w;
  for (int i = 0; i < h; ++i)
    cin >> s[i];
  for (int i = h - 1; i >= 0; --i) {
    for (int j = w - 1; j >= 0; --j) {
      if (i + j == h + w - 2) continue;
      if (~(i + j) & 1) { // Takahashi
        dp[i][j] = -1e7;
        if (j < w - 1) {
          if (s[i][j + 1] == '+') {
            dp[i][j] = max(dp[i][j], dp[i][j + 1] + 1);
          } else {
            dp[i][j] = max(dp[i][j], dp[i][j + 1] - 1);

          }
        }
        if (i < h - 1) {
          if (s[i + 1][j] == '+') {
            dp[i][j] = max(dp[i][j], dp[i + 1][j] + 1);
          } else {
            dp[i][j] = max(dp[i][j], dp[i + 1][j] - 1);
          }
        }
      } else {
        dp[i][j] = 1e7;
        if (j < w - 1) {
          if (s[i][j + 1] == '-') {
            dp[i][j] = min(dp[i][j], dp[i][j + 1] + 1);
          } else {
            dp[i][j] = min(dp[i][j], dp[i][j + 1] - 1);

          }
        }
        if (i < h - 1) {
          if (s[i + 1][j] == '-') {
            dp[i][j] = min(dp[i][j], dp[i + 1][j] + 1);
          } else {
            dp[i][j] = min(dp[i][j], dp[i + 1][j] - 1);
          }
        }
      }
    }
  }
  if (dp[0][0] > 0)
    cout << "Takahashi\n";
  if (dp[0][0] < 0)
    cout << "Aoki\n";
  if (dp[0][0] == 0)
    cout << "Draw\n";
}

int main() {
  int T = 1;
  /* cin >> T; */
  while (T--)
    solve();
  return 0;
}

