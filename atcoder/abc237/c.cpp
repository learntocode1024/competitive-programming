const int N = 1e6+5;
char s[N];

inline void solve() {
  cin >> s;
  int n = strlen(s);
  bool ok = 1;
  int i = 0, j = n - 1;
  int l = 0;
  while (s[i] == 'a') ++i, ++l;
  while (j && s[j] == 'a') --j, --l;
  if (j > i) {
    while (j > i) {
      if (s[j] != s[i]) ok = 0;
      --j; ++i;
    }
    if (l > 0) ok = 0;
  }
  println((ok)? "Yes":"No");
}

