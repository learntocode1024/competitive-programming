#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

int n;

void solve(int t, int rt, string str) {
  if (rt > t) return;
  if (t == n) {
    cout << str;
    for (int i = rt; i < n; ++i)
      cout << ')';
    cout << endl;
    return;
  }
  solve(t + 1, rt, str + "(");
  solve(t, rt + 1, str + ")");
}

int main() {
  cin >> n;
  if (n & 1) {
    return 0;
  }
  n >>= 1;
  solve(0, 0, "");
  return 0;
}

