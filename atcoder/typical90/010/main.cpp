#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

int a[MX][3];

void solve() {
  int n;
  cin >> n;
  for (int i = 1; i <= n; ++i) {
    int c;
    cin >> c >> a[i][c];
    a[i][1] += a[i - 1][1];
    a[i][2] += a[i - 1][2];
  }
  int q;
  cin >> q;
  while (q--) {
    int l, r;
    cin >> l >> r;
    --l;
    cout << a[r][1] - a[l][1] << " "
      << a[r][2] - a[l][2] << endl;
  }
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

