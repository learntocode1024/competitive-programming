#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <stack>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

vector<int> G[MX];
stack<int> s;
bool instack[MX];
int dfn[MX], low[MX], timer;
int cnt[MX], scc; 

void tarjan(int u) {
  low[u] = dfn[u] = ++timer;
  s.push(u);
  instack[u] = 1;
  for (auto v : G[u]) {
    if (!dfn[v]) {
      tarjan(v);
      low[u] = min(low[u], low[v]);
    } else if (instack[v])
        low[u] = min(low[u], dfn[v]);
  }
  if (dfn[u] == low[u]) {
    ++scc;
    int w;
    do {
      w = s.top();
      ++cnt[scc];
      instack[w] = 0;
      s.pop();
    } while (w != u);
  }
}

void solve() {
  int n; int m;
  cin >> n >> m;
  for (int i = 0; i < m; ++i) {
    int a, b;
    cin >> a >> b;
    G[a].push_back(b);
  }
  for (int i = 1; i <= n; ++i) {
    if (!dfn[i])
      tarjan(i);
  }
  LL ans = 0;
  for (int i = 1; i <= scc; ++i) {
    ans += 1ll * cnt[i] * (cnt[i] - 1) / 2;
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

