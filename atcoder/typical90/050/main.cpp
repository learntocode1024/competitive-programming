#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 3000005
const long long mod = 1000000007;

pair<int, int> fib(LL n) {
  if (n == 0) return {0, 1};
  auto p = fib(n >> 1);
  LL t1 = p.first * ((p.second << 1) - (p.first));
  LL t2 = p.second * p.second + p.first * p.first;
  t1 %= mod;
  t2 %= mod;
  if (n & 1)
    return {t2, (t2 + t1) % mod};
  return {t1, t2};
}

LL dp[MX];

void solve() {
  int n, l;
  cin >> n >> l;
  dp[0] = 1;
  for (int i = 1; i <= n; ++i) {
    dp[i] = dp[i - 1];
    if (i >= l)
      dp[i] += dp[i - l];
    dp[i] %= mod;
  }
  cout << dp[n] << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

