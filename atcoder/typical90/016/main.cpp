#include <algorithm>
#include <climits>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

void solve() {
  LL n;
  cin >> n;
  LL c[3];
  for (int i = 0; i < 3; ++i)
    cin >> c[i];
  sort(c, c + 3);
  LL ans = INT_MAX;
  for (int i = n / c[2]; i >= 0; --i) {
    if (i + (n - i * c[2]) / c[1] >= 9999)
      continue;
    for (int j = (n - c[2] * i) / c[1]; j >= 0; --j) {
      if ((n - c[2] * i - c[1] * j) % c[0] == 0) {
        ans = min(ans, i + j + (n - c[2] * i - c[1] * j) / c[0]);
      }
    }
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

