#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 300005

pair<int, int> a[MX];

int n, m;
int b[MX];

int get(int x) {
  int ret = 0;
  while (x) {
    ret += b[x];
    x -= x & -x;
  }
  return ret;
}

void ins(int x, int v) {
  while (x <= n) {
    b[x] += v;
    x += x & -x;
  }
}

void solve() {
  cin >> n >> m;
  for (int i = 0; i < m; ++i) {
    cin >> a[i].second >> a[i].first;
    a[i].second = -a[i].second;
  }
  sort(a, a + m);
  LL ans = 0;
  for (int i = 0; i < m; ++i) {
    int l = -a[i].second, r = a[i].first;
    ans += get(l);
    if (r - l > 1)
      ins(l + 1, 1), ins(r, -1);
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

