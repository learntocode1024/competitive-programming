#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 45

int n, k;
LL p;
LL a[MX];

vector<LL> v1[MX], v2[MX];

void solve() {
  cin >> n >> k >> p;
  for (int i = 0; i < n; ++i) {
    cin >> a[i];
  }
  random_shuffle(a, a + n);
  int mid = n / 2 + 1;
  for (int i = 0; i < (1 << mid); ++i) {
    LL tmp = 0;
    for (int j = 0; j < mid; ++j) {
      tmp += a[j] * ((i & (1 << j)) != 0);
    }
    v1[__builtin_popcount(i)].push_back(tmp);
  }
  for (int i = 0; i < (1 << (n - mid)); ++i) {
    LL tmp = 0;
    for (int j = 0; j < (n - mid); ++j) {
      tmp += a[j + mid] * ((i & (1 << j)) != 0);
    }
    v2[__builtin_popcount(i)].push_back(tmp);
  }
  for (int i = 1; i <= k; ++i) {
    sort(v1[i].begin(), v1[i].end());
    sort(v2[i].begin(), v2[i].end());
  }
  LL cnt = 0;
  cnt += upper_bound(v1[k].begin(), v1[k].end(), p) - v1[k].begin();
  cnt += upper_bound(v2[k].begin(), v2[k].end(), p) - v2[k].begin();
  for (int i = 1; i < k; ++i) {
    for (int j = 0; j < v1[i].size() && v1[i][j] < p; ++j) {
      cnt += upper_bound(v2[k - i].begin(), v2[k - i].end(), p - v1[i][j]) - v2[k - i].begin();
    }
  }
  cout << cnt << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

