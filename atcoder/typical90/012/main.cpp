#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 4000005

// clang-format off
const string YES = "Yes";
const string NO = "No";
#define FALSE_EXIT \
  { cout << NO << endl; return; }
// clang-format on

int fa[MX];
bool isred[MX];

int dsu_find(int x) {
  if (x != fa[x])
    fa[x] = dsu_find(fa[x]);
  return fa[x];
}

void solve() {
  int h, w;
  cin >> h >> w;
  for (int i = 0; i < h * w; ++i)
    fa[i] = i;
  int q;
  cin >> q;
  while (q--) {
    int t, r, c;
    cin >> t >> r >> c;
    --r, --c;
    if (t == 1) {
      int cur = r * w + c;
      isred[cur] = 1;
      int cen = dsu_find(cur);
      if (c && isred[cur - 1])
        fa[dsu_find(cur - 1)] = cen;
      if (c + 1 != w && isred[cur + 1])
        fa[dsu_find(cur + 1)] = cen;
      if (r && isred[cur - w])
        fa[dsu_find(cur - w)] = cen;
      if (r + 1 != h && isred[cur + w])
        fa[dsu_find(cur + w)] = cen;
    } else {
      int r1, c1;
      cin >> r1 >> c1;
      --r1, --c1;
      cout << ((dsu_find(r * w + c) == dsu_find(r1 * w + c1) &&
                isred[r1 * w + c1] && isred[r * w + c])
                   ? YES
                   : NO)
           << endl;
    }
  }
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

