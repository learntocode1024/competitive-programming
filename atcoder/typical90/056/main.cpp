#include <iostream>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX
const string NO = "Impossible";
#define FALSE_EXIT                                                             \
  {                                                                            \
    cout << NO << endl;                                                        \
    return;                                                                    \
  }

int a[101], b[101];

void solve() {
  int n, s;
  cin >> n >> s;
  bitset<100000> t[101];
  t[0].set(0);
  for (int i = 1; i <= n; ++i) {
    cin >> a[i] >> b[i];
    t[i] = (t[i - 1] << a[i]) | (t[i - 1] << b[i]);
  }
  if (!t[n][s]) FALSE_EXIT;
  string ans;
  for (int i = n; i; --i) {
    if (s >= a[i] && t[i - 1][s - a[i]]) {
      s -= a[i];
      ans.push_back('A');
    } else {
      s -= b[i];
      ans.push_back('B');
    }
  }
  reverse(begin(ans), end(ans));
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

