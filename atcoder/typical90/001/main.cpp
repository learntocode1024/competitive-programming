#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

LL a[MX], L;
int n, k;

bool check(LL k) {
  int cnt = 0;
  LL prev = 0;
  for (int i = 0; i <= n; ++i) {
    if (a[i] - prev >= k) {
      ++cnt;
      prev = a[i];
    }
  }
  return cnt > ::k;
 }

void solve() {
  cin >> n >> L;
  cin >> k;
  for (int i = 0; i < n; ++i) {
    cin >> a[i];
  }
  a[n] = L;
  LL l = 0, r = L + 1;
  while (l + 1 < r) {
    LL mid = l + (r - l) / 2;
    if (check(mid)) l = mid;
    else r = mid;
  }
  cout << l << endl;
}

int main() {
  int T = 1;
  /* cin >> T; */
  while (T--)
    solve();
  return 0;
}

