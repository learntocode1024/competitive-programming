#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 200005

int a[MX << 1];

void solve() {
  int n, k;
  cin >> n >> k;
  for (int i = 0; i < n; ++i) {
    int x, b;
    cin >> x >> b;
    a[i] = -b;
    a[n + i] = -x + b;
  }
  sort(a, a + 2 * n);
  LL ret = 0;
  for (int i = 0; i < k; ++i) {
    ret -= a[i];
  }
  cout << ret << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

