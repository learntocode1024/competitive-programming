#include <algorithm>
#include <bitset>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX
const long long mod = 998244353;

bitset<300> c[305], to;

void solve() {
  int n, m;
  cin >> n >> m;
  for (int i = 0; i < n; ++i) {
    int t;
    cin >> t;
    while (t--) {
      int p;
      cin >> p;
      c[i].set(p - 1);
    }
  }
  for (int i = 0; i < m; ++i) {
    bool b;
    cin >> b;
    to[i] = b;
  }
  c[n] = to;
  // GAUSS
  for (int i = 0; i < m; ++i) {
    int r = 0;
    while (i != c[r]._Find_first() && r < n) ++r;
    if (r == n) continue;
    for (int j = 0; j < n + 1; ++j) {
      if (r != j && c[j][i]) c[j] ^= c[r];
    }
  }
  LL ans = 1;
  for (int i = 0; i < n; ++i) {
    if (c[i].none()) {
      ans <<= 1;
      ans %= mod;
    }
  }
  cout << ans * c[n].none() << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

