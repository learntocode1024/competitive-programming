#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005
const long long mod = 1000000007;

LL fac[MX], inv[MX], ifac[MX];

LL calc(int n, int k) {
  LL ret = 0;
  for (int t = 1; t <= n / k + (n % k != 0); ++t) {
    int m = n - (t - 1) * k - 1;
    ret += fac[m + t] * ifac[m] % mod * ifac[t] % mod;
    ret %= mod;
  }
  return ret;
}

void solve() {
  long long N;
  scanf("%lld", &N);
  fac[1] = inv[1] = ifac[1] = 1;
  fac[0] = ifac[0] = 1;
  for (int i = 2; i <= N; ++i) {
    fac[i] = fac[i - 1] * i % mod;
    inv[i] = (mod - mod / i) * inv[mod % i] % mod;
    ifac[i] = ifac[i - 1] * inv[i] % mod;
  }
  for (int k = 1; k <= N; ++k) {
    cout << calc(N, k) << endl;
  }
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

