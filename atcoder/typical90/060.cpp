#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 300005

int N;

int a[MX], b[MX], c[MX];
int d1[MX], d2[MX];

int get(int* buf, int x) {
  int ret = 0;
  while (x) {
    ret = max(ret, buf[x]);
    x -= x & -x;
  }
  return ret;
}

void ins(int* buf, int x, int val) {
  while (x <= N) {
    buf[x] = max(buf[x], val);
    x += x & -x;
  }
}

void solve() {
  cin >> N;
  for (int i = 0; i < N; ++i)
    cin >> a[i + 1];
  for (int i = 1; i <= N; ++i) {
    d1[i] = get(b, a[i] - 1) + 1;
    ins(b, a[i], d1[i]);
  }
  int ans = 0;
  for (int i = N; i; --i) {
    d2[i] = get(c, a[i] - 1) + 1;
    ins(c, a[i], d2[i]);
    ans = max(ans, d1[i] + d2[i] - 1);
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

