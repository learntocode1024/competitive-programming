using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

vector<pair<int, int>> G[MX];

void dijkstra(vector<int> &dist, int ori) {
  priority_queue<pair<int, int>> q;
  for (int i = 1; i <= dist.size(); ++i)
    dist[i] = INT_MAX;
  vector<bool> vis(dist.size(), 0);
  q.push({0, ori});
  dist[ori] = 0;
  while (!q.empty()) {
    int u = q.top().second;
    q.pop();
    if (vis[u])
      continue;
    vis[u] = 1;
    for (auto i : G[u]) {
      int v = i.first;
      dist[v] = min(dist[v], dist[u] + i.second);
      q.push({-dist[v], v});
    }
  }
}
vector<int> d1(MX), d2(MX);

void solve() {
  int n, m;
  cin >> n >> m;
  for (int i = 0; i < m; ++i) {
    int a, b, c;
    cin >> a >> b >> c;
    G[a].push_back({b, c});
    G[b].push_back({a, c});
  }
    dijkstra(d1, 1);
  dijkstra(d2, n);
  for (int i = 1; i <= n; ++i) {
    cout << d1[i] + d2[i] << endl;
  }
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

