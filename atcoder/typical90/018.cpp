#include <algorithm>
#include <cmath>
#include <cstdio>
#include <cstring>
#include <iomanip>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

void solve() {
  double X, Y, T, l;
  cin >> T >> l >> X >> Y;
  int q;
  cin >> q;
  X = X * X;
  while (q--) {
    double t;
    cin >> t;
    double ang = 2 * M_PI * t / T;
    double z = l / 2 * (1 - cos(ang));
    double y = -l / 2 * sin(ang);
    double dist = sqrt(X + (Y - y) * (Y - y));
    printf("%.12lf\n", atan(z / dist) * M_1_PI * 180);
  }
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

