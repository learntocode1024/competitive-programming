#include <algorithm>
#include <bitset>
#include <cstdint>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100000

vector<int> G[MX];
int a[MX], b[MX];

const string YES = "Yes";
const string NO = "No";
#define FALSE_EXIT                                                             \
  {                                                                            \
    cout << NO << endl;                                                        \
    return;                                                                    \
  }

void solve() {
  int n, m, q;
  cin >> n >> m >> q;
  for (int i = 0; i < m; ++i) {
    int x, y;
    cin >> x >> y;
    --x, --y;
    G[x].push_back(y);
  }
  for (int i = 0; i < q; ++i) {
    cin >> a[i] >> b[i];
    --a[i], --b[i];
  }
  for (int i = 0; (i << 6) < q; ++i) {
    vector<uint64_t> dp(n);
    for (int j = i << 6; j < min((i + 1) << 6, q); ++j) { // This is how we solve 64 queries a time
      dp[b[j]] |= (1ull << (j - (i << 6)));
    }
    for (int u = n - 1; u >= 0; --u) {
      for (auto v : G[u]) {
        dp[u] |= dp[v];
      }
    }
    for (int j = i << 6; j < min((i + 1) << 6, q); ++j) {
      cout << ((dp[a[j]] & (1ull << (j - (i << 6)))) ? YES : NO) << endl;
    }

  }
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

