#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 600005

vector<pair<int, int> > a;
int ans[MX];

void solve() {
  int n;
  cin >> n;
  for (int i = 0; i < n; ++i) {
    int tmp;
    cin >> tmp;
    a.push_back({tmp, 0});
  }
  int q;
  cin >> q;
  for (int i = 1; i <= q; ++i) {
    int tmp;
    cin >> tmp;
    a.push_back({tmp, i});
  }
  sort(begin(a), end(a));
  int tmp = -1e9;
  for (auto i : a) {
    if (!i.second) tmp = i.first;
    else {
      ans[i.second] = i.first - tmp;
    }
  }
  tmp = 2e9;
  for (auto i = rbegin(a); i != rend(a); ++i) {
    if (!i->second) tmp = i->first;
    else {
      ans[i->second] = min(ans[i->second], tmp - i->first);
    }
  }
  for (int i = 1; i <= q; ++i) {
    cout << ans[i] << endl;
  }
}

int main() {
  int T = 1;
  /* cin >> T; */
  while (T--)
    solve();
  return 0;
}

