#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 46

LL a[4][MX];

void solve() {
  int n;
  cin >> n;
  for (int i = 0; i < 3; ++i) {
    for (int j = 0; j < n; ++j) {
      int tmp;
      cin >> tmp;
      ++a[i][tmp % 46];
    }
  }
  for (int i = 0; i < 46; ++i) {
    for (int j = 0; j < 46; ++j) {
      for (int k = 0; k < 46; ++k) {
        a[3][(i + j + k) % 46] += a[0][i] * a[1][j] * a[2][k];
      }
    }
  }
  cout << a[3][0] << endl;
}

int main() {
  int T = 1;
  /* cin >> T; */
  while (T--)
    solve();
  return 0;
}

