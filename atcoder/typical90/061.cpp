#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

// clang-format off
// clang-format on

int a[MX], b[MX];
int l, r;

void solve() {
  int q;
  cin >> q;
  int t, x;
  while (q--) {
    cin >> t >> x;
    switch(t) {
      case 1:
        a[l++] = x;
        continue;
      case 2:
        b[r++] = x;
        continue;
      case 3:
        cout << ((x <= l) ? a[l - x] : b[x - l - 1]) << endl;
        continue;
    }
  }
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

