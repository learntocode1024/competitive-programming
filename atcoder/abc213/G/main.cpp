#include <algorithm>
#include <cctype>
#include <climits>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 20

// clang-format off
const long long mod = 998244353;
// clang-format on
bool g[MX][MX];
LL dp[1 << 20];
int n, m;

int ans[MX];

void solve() {
  cin >> n >> m;
  dp[1] = 1;
  for (int i = 0, a, b; i < m; ++i) {
    cin >> a >> b;
    g[a][b] = g[b][a] = 1;
  }
  for (int i = 3; i < (1 << n); i += 2) {
    for (int u = 2; u <= n; ++u) {
      if ((i & (1 << (u - 1))) == 0 ||!dp[i - (1 << (u - 1))]) continue;
      int cnt = 0;
      for (int v = 0; v < n; ++v) {
        if (i & (1 << v)) cnt += g[u][v + 1];
      }
      dp[i] = 1ll * dp[i - (1 << (u - 1))] * ((1 << cnt) - 1) % mod;
      break;
    }
  }  
  for (int i = 3; i < (1 << n); i += 2) {
    LL fac = m;
    for (int j = 1; j <= n; ++j) {
      if (i & (1 << (j - 1))) {
        for (int k = j + 1; k <= n; ++k) fac -= g[j][k];
      }
    }
    fac = (1 << fac) % mod;
    for (int j = 2; j <= n; ++j) {
      if (i & (1 << (j - 1))) {
        ans[j] = (ans[j] + dp[i] * fac) % mod;
      }
    }
  }
  for (int i = 2; i <= n; ++i) {
    cout << ans[i] << endl;
  }
}

int main() {
  int T = 1;
  while (T--) solve();
  return 0;
}

