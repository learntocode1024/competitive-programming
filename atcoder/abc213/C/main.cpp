#include <algorithm>
#include <cctype>
#include <climits>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

// clang-format off
// clang-format on

int x[MX], y[MX], dx[MX], dy[MX];

void solve() {
  int n, t;
  cin >> t >> t >> n;
  for (int i = 0; i < n; ++i) {
    cin >> x[i] >> y[i];
    dx[i] = x[i];
    dy[i] = y[i];
  }
  sort(dx, dx + n);
  sort(dy, dy + n);
  int xx = unique(dx, dx + n) - dx;
  int yy = unique(dy, dy + n) - dy;
  for (int i = 0; i < n; ++i) {
    cout << lower_bound(dx, dx + xx, x[i]) - dx + 1 << ' '
         << lower_bound(dy, dy + yy, y[i]) - dy + 1 << endl;
  }
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

