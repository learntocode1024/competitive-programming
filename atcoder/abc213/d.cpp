#include <algorithm>
#include <cctype>
#include <climits>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 200005

// clang-format off
// clang-format on

vector<int> G[MX];

void dfs(int u, int fa) {
  cout << u << ' ';
  for (auto v : G[u]) {
    if (v == fa) continue;
    dfs(v, u);
    cout << u << ' ';
  }
}

void solve() {
  int n;
  cin >> n;
  for (int i = 1, a, b; i < n; ++i) {
    cin >> a >> b;
    G[a].pb(b);
    G[b].pb(a);
  }
  for (int i = 1; i <= n; ++i) {
    sort(begin(G[i]), end(G[i]));
  }
  dfs(1, 0);
  cout << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

