#include <climits>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 2005

// clang-format off
// clang-format on

int l[MX], r[MX];

void solve() {
  int n;
  cin >> n;
  for (int i = 0; i < n; ++i) {
    int t;
    cin >> t >> l[i] >> r[i];
    l[i] += (t > 2);
    r[i] += (~t & 1);
  }
  int ans = 0;
  for (int i = 0; i < n; ++i) {
    int a = l[i], b = r[i];
    for (int j = i + 1; j < n; ++j) {
      /* ans += (l[j] < a) && (r[j] > a) && (r[j] < b); */
      /* ans += (l[j] > a) && (l[j] < b) && (r[j] > b); */
      ans += (l[j] < b) && (r[j] > a);
    }
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

