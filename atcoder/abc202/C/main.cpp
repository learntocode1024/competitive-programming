#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

LL a[MX], b[MX], rb[MX], c[MX];

void solve() {
  int n;
  cin >> n;
  for (int i = 0; i < n; ++i)
    cin >> a[i], --a[i];
  for (int i = 0; i < n; ++i)
    cin >> b[i], --b[i];
  for (int i = 0; i < n; ++i) 
    cin >> c[i], --c[i];
  for (int i = 0; i < n; ++i) {
    ++rb[b[c[i]]];
  }
  LL ans = 0;
  for (int i = 0; i < n; ++i) {
    ans += rb[a[i]];
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  /* cin >> T; */
  while (T--)
    solve();
  return 0;
}

