using namespace std;
typedef __int128 LL;
typedef unsigned long long ULL;

__int128 MAX = INT64_MAX;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

int id[128];
vector<LL> v, vc[3];

int cnt[3];
__int128 read() {
    __int128 x = 0, f = 1;
    char ch = getchar();
    while (ch < '0' || ch > '9') {
        if (ch == '-') f = -1;
        ch = getchar();
    }
    while (ch >= '0' && ch <= '9') {
        x = x * 10 + ch - '0';
        ch = getchar();
    }
    return x * f;
}
std::ostream& operator<<(std::ostream& os, const __int128 i) noexcept
{
  std::ostream::sentry s(os);
  if (s) {
    unsigned __int128 tmp = i < 0 ? -i : i;
    char buffer[128];
    char *d = std::end(buffer);
    do {
      --d;
      *d = "0123456789"[tmp % 10];
      tmp /= 10;
    } while (tmp != 0);
    if (i < 0) {
      --d;
      *d = '-';
    }
    int len = std::end(buffer) - d;
    if (os.rdbuf()->sputn(d, len) != len) {
      os.setstate(std::ios_base::badbit);
    }
  }
  return os;
}

void solve() {
  MAX <<= 20;
  id['R'] = 0;
  id['G'] = 1;
  id['B'] = 2;
  int n;
  cin >> n;
  n *= 2;
  for (int i = 0; i < n; ++i) {
    char c;
    LL t;
    t = read();
    cin >> c;
    v.push_back(t);
    vc[id[c]].push_back(t);
    ++cnt[id[c]];
  }
  if ((~cnt[0] & 1) && (~cnt[1] & 1)) {
    cout << 0 << endl;
    return;
  }
  if (~cnt[0] & 1)
    swap(vc[0], vc[2]);
  if (~cnt[1] & 1)
    swap(vc[1], vc[2]);
  vector<LL> &a = vc[0], &b = vc[1];
  sort(begin(a), end(a));
  sort(begin(b), end(b));
  sort(begin(v), end(v));
  LL ans = 0;
  LL m = MAX;
  for (auto i : a) {
    if (i != v[0]) m = min(m, i - *(lower_bound(begin(v), end(v), i) - 1));
    if (i != v[n - 1]) m = min(m, *upper_bound(begin(v), end(v), i) - i);
  }
  ans += m;
  m = MAX;
  for (auto i : b) {
    if (i != v[0]) m = min(m, i - *(lower_bound(begin(v), end(v), i) - 1));
    if (i != v[n - 1]) m = min(m, *upper_bound(begin(v), end(v), i) - i);
  }
  ans += m;
  for (auto i : a) {
    auto it = lower_bound(begin(b), end(b), i);
    if (it != end(b))
      ans = min(ans, *it - i);
    if (it == begin(b))
      continue;
    --it;
    ans = min(ans, i - *it);
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

