#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <iterator>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

vector<int> a, ans;

void solve() {
  int n;
  cin >> n;
  a.clear();
  ans.clear();
  for (int i = 0; i < n; ++i) {
    a.push_back(0);
    cin >> a[i];
  }
  int T = 0;
  while (!is_sorted(begin(a), end(a))) {
    ++T;
    for (int i = 0; i < n; ++i) {
      if (((T & 1) == ((i + 1) & 1)) && a[i] > i + 1) {
        swap(a[i], a[i + 1]);
        ans.push_back(i + 1);
        break;
      }
    }
  }
  cout << ans.size() << endl;
  for (auto i : ans) {
    cout << i << ' ';
  }
  cout << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

