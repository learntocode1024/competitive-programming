#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

vector<pair<int, int> > x, y;

int operator- (const pair<int, int> a, const pair<int, int> b) {
  return a.first - b.first;
}

void solve() {
  int n;
  cin >> n;
  for (int i = 0; i < n; ++i) {
    int xi, yi;
    cin >> xi >> yi;
    x.push_back({xi, i});
    y.push_back({yi, i});
  }
  sort(begin(x), end(x));
  sort(begin(y), end(y));
  int ans = 0;
  if ((x[n - 1].first - x[0].first) > (y[n - 1].first - y[0].first)) {
    ans = max(x[n - 2].first - x[0].first, x[n - 1].first - x[1].first);
    if ((x[n - 1].second == y[n - 1].second || x[n - 1].second == y[0].second)
        && (x[0].second == y[n - 1].second || x[0].second == y[0].second)) {
      ans = max({ans, y[n - 2] - y[0], y[n - 1] - y[1]});
    } else {
      ans = max(ans, y[n - 1] - y[0]);
    }
  } else {
    ans = max(y[n - 2].first - y[0].first, y[n - 1].first - y[1].first);
    if ((x[n - 1].second == y[n - 1].second || x[n - 1].second == y[0].second)
        && (x[0].second == y[n - 1].second || x[0].second == y[0].second)) {
        ans = max({ans, x[n - 2] - x[0], x[n - 1] - x[1]});
    } else {
      ans = max(ans, x[n - 1] - x[0]);
    }
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

