#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 2005

vector<int> G[MX];

// clang-format off
// clang-format on

vector<bool> vis;

int dfs(int u) {
  vis[u] = 1;
  int ret = 1;
  for (auto v : G[u]) {
    if (!vis[v]) ret += dfs(v);
  }
  return ret;
}

void solve() {
  int n, m;
  cin >> n >> m;
  for (int i = 0; i < m; ++i) {
    int a, b;
    cin >> a >> b;
    --a, --b;
    G[a].push_back(b);
  }
  int ans = 0;
  for (int i = 0; i < n; ++i) {
    vis = vector<bool>(n);
    ans += dfs(i);
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

