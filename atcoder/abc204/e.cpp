#include <algorithm>
#include <cmath>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <queue>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

// clang-format off
// clang-format on

int n, m;
vector<pair<int, int>> G[MX];
LL c[MX], d[MX];
bool vis[MX];

LL minT(int id, LL t) {
  LL ret = 1e18;
  for (LL i = max(t, (LL)floor(sqrt(d[id])) - 2);
       i <= max(t, (LL)floor(sqrt(d[id])) + 2); ++i) {
    ret = min(1.0l * ret, i + floor(1.0l * d[id] / (i + 1)));
  }
  return ret + c[id];
}

LL bfs() {
  priority_queue<pair<LL, int>> q;
  q.push({0, 1});
  vector<LL> T(n + 1, 1e18);
  T[1] = 0;
  while (!q.empty()) {
    int p = q.top().second;
    q.pop();
    if (vis[p])
      continue;
    vis[p] = 1;
    for (auto i : G[p]) {
      int v = i.first;
      T[v] = min(T[v], minT(i.second, T[p]));
      q.push({-T[v], v});
    }
  }
  return (T[n] == 1e18 ? -1 : T[n]);
}

void solve() {
  cin >> n >> m;
  for (int i = 0; i < m; ++i) {
    int a, b;
    cin >> a >> b >> c[i] >> d[i];
    G[a].push_back({b, i});
    G[b].push_back({a, i});
  }
  cout << bfs() << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

