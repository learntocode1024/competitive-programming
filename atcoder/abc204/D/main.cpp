#include <algorithm>
#include <bitset>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

bitset<MX> b;

void solve() {
  int n;
  cin >> n;
  vector<int> a(n);
  b.set(0);
  int total = 0;
  for (int i = 0; i < n; ++i) {
    cin >> a[i];
    total += a[i];
    b |= (b << a[i]);
  }
  int c = total >> 1;
  int diff = MX;
  int d = 0;
  for (int i = 0; i < MX; ++i) {
    if (b[i]) {
      if (abs(i - c) < diff) 
        diff = abs(i - c), d = i;
    }
  }
  cout << max(d, total - d) << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

