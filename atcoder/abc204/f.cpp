#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
typedef vector<vector<LL> > mat;

#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

const LL mod = 998244353;
// clang-format off
// clang-format on

int H, h;

mat mul(const mat a, const mat b) {
  mat ret(H, vector<LL>(H));
  for (int i = 0; i < H; ++i) {
    for (int j = 0; j < H; ++j) {
      for (int k = 0; k < H; ++k) {
        ret[i][j] += a[i][k] * b[k][j];
        ret[i][j] %= mod;
      }
    }
  }
  return ret;
}

mat Q_pow(mat a, LL T) {
  mat ret(H, vector<LL>(H));
  for (int i = 0; i < H; ++i) {
    ret[i][i] = 1;
  }
  while (T) {
    if (T & 1) ret = mul(ret, a);
    a = mul(a, a);
    T >>= 1;
  }
  return ret;  
}

inline int helper(int i, int j) {
  if ((i & j) != i)
    return 0;
  i ^= j;
  int dp[6];
  fill(dp, dp + 6, 0);
  dp[0] = 1;
  dp[1] = 1 + ((i & 3) == 3);
  for (int k = 2; k < h; ++k) {
    dp[k] = dp[k - 1] + dp[k - 2] * ((i >> (k - 1) & 3) == 3);
  }
  return dp[h - 1];
}

void solve() {
  long long w;
  cin >> h >> w;
  H = (1 << h);
  mat m1(H, vector<LL>(H));
  mat m2(H, vector<LL>(H));
  for (int i = 0; i < H; ++i) {
    for (int j = 0; j < H; ++j) {
      m1[j][i] = helper(i, j);
    }
  }
  for (int i = 0; i < H; ++i) {
    m2[(H - 1) ^ i][i] = 1;
  }
  mat m = mul(m2, m1);
  m = Q_pow(m, w);
  LL ans = 0;
  for (int i = 0; i < H; ++i) {
    ans += m[i][0];
    ans %= mod;
  }
  cout << m[0][0] << endl;
}

int main() {
  int T = 1;
  while (T--)
    solve();
  return 0;
}

