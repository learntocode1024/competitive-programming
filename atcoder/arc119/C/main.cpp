#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <unordered_map>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x)                                                              \
  if ((bool)x)                                                                 \
    printf("YES");                                                             \
  else                                                                         \
    printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 300005

LL a[MX];

LL bin(int l, int r) {
  if (r == l + 1)
    return 0;
  unordered_map<LL, int> s;
  int mid = l + (r - l) / 2;
  LL sum = 0;
  for (int i = mid - 1; i >= l; --i) {
    sum += a[i];
    ++s[sum];
  }
  LL ret = 0;
  sum = 0;
  for (int i = mid; i < r; ++i) {
    sum += a[i];
    ret += s[-sum];
  }
  ret += bin(l, mid);
  ret += bin(mid, r);
  return ret;
}

void solve() {
  int n;
  cin >> n;
  for (int i = 0; i < n; ++i) {
    cin >> a[i];
    if (i & 1) a[i] = -a[i];
  }
  LL ans = bin(0, n);
  cout << ans << endl;
}

int main() {
  int T = 1;
  /* cin >> T; */
  while (T--)
    solve();
  return 0;
}

