#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
#include <cmath>
#include <deque>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

LL a[2005][2005];
LL b[2005][2005];
LL c[2005][2005];
LL d[2005][2005];

template<typename T> class slide {
private:
  int len = 0;
  int curr = 0;
  deque<pair<T, int> > Q;
public:
  slide(const int length) : len(length), Q() {}; 
  void push(const T &val) {
    while (!Q.empty() && Q.front().second < curr - len)
      Q.pop_front();
    while (!Q.empty() && Q.back().first <= val)
      Q.pop_back();
    Q.push_back({val, curr});
    ++curr;
  }
  T top() const {
    return Q.front().first;
  }
};

void solve() {
  int n, k, stride;
  cin >> n >> k >> stride;
  for (int i = 0; i < n; ++i) {
    for (int j = 0; j < n; ++j) {
      cin >> a[i][j];
    }
  }
  for (int i = 0; i < n; ++i) {
    slide<LL> s(k);
    for (int j = 0; j < k; ++j) {
      s.push(a[i][j]);
    }
    for (int j = 0; j <= n - k; ++j) {
      if (j % stride == 0) {
        for (int l = ceil(1.0l * (i - k + 1) / stride); l <= floor(1.0l * i / stride); ++l)
          b[l][j / stride] = max(b[l][j / stride], s.top());
      }
      s.push(a[i][j + k]);
    }
  }
  for (int i = 0; i < n; ++i) {
    for (int j = 0; j < n; ++j) {
      d[i][j] = a[i][j];
      if (j) d[i][j] += d[i][j - 1];
    }
  }
  for (int i = 0; i < n; ++i) {
    for (int j = 0; j < n; ++j) {
      if (i)
        d[i][j] += d[i - 1][j];
    }
  }
  for (int i = 0; i < (n - k) / stride + 1; ++i) {
    for (int j = 0; j < (n - k) / stride + 1; ++j) {
      c[i][j] = d[i * stride + k - 1][j * stride + k - 1];
      if (i) c[i][j] -= d[i * stride - 1][j * stride + k - 1];
      if (j) c[i][j] -= d[i * stride + k - 1][j * stride - 1];
      if (i && j) c[i][j] += d[i * stride - 1][j * stride - 1];
      // c[i][j] /= k * k;
    }
  }
  for (int i = 0; i < (n - k) / stride + 1; ++i) {
    LL ans = 0, p39 = 1;
    for (int j = 0; j < (n - k) / stride + 1; ++j) {
      ans = (ans + p39 * b[i][j]) % 998244353;
      p39 = p39 * 39 % 998244353;
    }
    cout << ans << endl;
  }
  for (int i = 0; i < (n - k) / stride + 1; ++i) {
    LL ans = 0, p37 = 1;
    for (int j = 0; j < (n - k) / stride + 1; ++j) {
      ans = (ans + p37 * c[i][j]) % 1000000007;
      p37 = p37 * 37 % 1000000007;
    }
    cout << ans << endl;
  }
}

int main() {
  int T = 1;
  // cin >> T;
  while (T--)
    solve();
  return 0;
}

