const double PI = 3.141592653589l;

struct vec3 {
  double x, y, z;
  vec3() : x(0), y(0), z(0) {};
  vec3(double a, double b, double c) : x(a), y(b), z(c) {};
};

double dist2(const vec3 &a, const vec3 &b) {
  double ans = (a.x - b.x) * (a.x - b.x);
  ans += (a.y - b.y) * (a.y - b.y);
  ans += (a.z - b.z) * (a.z - b.z);
  return ans;
}

double dist(const vec3 &a, const vec3 &b) {
  return sqrt(dist2(a, b));
}

void solve() {
  double l1, l2, px, py, pz;
  cin >> l1 >> l2 >> px >> py >> pz;
  if (px == 0 && py == 0 && pz == 0 && l1 == l2) {
    cout << 0 << endl;
    return;
  }
  vec3 P(px, py, pz);
  vec3 O;
  double d2 = dist2(P, O);
  double d = dist(P, O);
  if (d - l1 - l2 > 0 || l1 - l2 - d > 0 || l2 - l1 - d > 0) {
    cout << -1 << endl;
    return;
  }
  double dt = (d2 + l1 * l1 - l2 * l2) / (2.0l * d * l1);
  dt = acos(dt);
  double pt = asin(abs(pz / d));
  if (pz < 0) pt = -pt;
  double tl = PI / 2 + pt - dt;
  cout << setprecision(12) << abs(tl) * l1 << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

