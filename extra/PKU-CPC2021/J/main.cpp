#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

vector<LL> h;

void solve() {
  int n;
  LL A;
  cin >> n >> A;
  h = vector<LL>(n);
  for (int i = 0; i < n; ++i) {
    cin >> h[i];
  }
  sort(h.begin(), h.end());
  LL len = 0;
  LL nil = A * (n - 1);
  for (int i = n - 1; i >= 0; --i) {
    nil = min(nil, A * i);
    len += max(0ll, - A * i + h[i] + nil);
    nil = min(nil, A * i - h[i]);
  }
  cout << len << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

