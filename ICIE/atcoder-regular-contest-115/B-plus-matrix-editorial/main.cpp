#include <algorithm>
#include <cstdio>
#include <iostream>
using namespace std;
#define printb(x) \
  if ((bool)x) printf("Yes\n"); \
  else printf("No\n");
#define MX 505

int a[MX], b[MX];

int main() {
  int n;
  cin >> n;
  int minv = 1e9 + 7;
  for (int i = 0; i < n; ++i) {
    cin >> a[i];
    minv = min(minv, a[i]);
  }
  b[0] = minv;
  for (int i = 0; i < n; ++i) {
    a[i] -= minv;
  }
  bool ans = true;
  for (int i = 1; i < n; ++i) {
    int curr;
    cin >> curr;
    b[i] = curr - a[0];
    for (int j = 1; j < n; ++j) {
      cin >> curr;
      ans = ans && (curr - a[j] == b[i]);
    }
  }
  printb(ans);
  if (ans) {
    for (int i = 0; i < n; ++i) {
      printf("%d ", b[i]);
    }
    puts("");
    for (int i = 0; i < n; ++i) {
      printf("%d ", a[i]);
    }
    puts("");
  }
  return 0;
}

