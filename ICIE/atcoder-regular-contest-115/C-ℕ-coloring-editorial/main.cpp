#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
#define MX 100005

int out[MX], pri[MX], vis[MX], cnt;

int main() {
  int n;
  cin >> n;
  out[1] = vis[1] = 1;
  for (int i = 2; i <= n; ++i) {
    if (!vis[i]) {
      vis[i] = i;
      pri[cnt++] = i;
      out[i] = 1;
    }
    for (int k = 0; k < cnt; ++k) {
      if (pri[k] > vis[i] || pri[k] * i > n) break;
      vis[pri[k] * i] = pri[k];
       
    }
  }
  return 0;
}

