#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 105
LL a[MX][MX], b[MX * MX], c[MX][MX];

void solve() {
  int n, m;
  cin >> n >> m;
  for (int i = 0; i < n; ++i) {
    for (int j = 0; j < m; ++j) {
      cin >> a[i][j];
      b[i * m + j] = a[i][j];
    }
  }
  sort(b, b + m * n);
  LL lim = b[m - 1];
  int cnt = 0;
  for (int i = m - 1; i >= 0 && b[i] == lim; --i) ++cnt;
  int off = 0;
  for (int i = 0; i < n; ++i) {
    sort(a[i], a[i] + m);
    for (int j = 0; j < m; ++j) {
      c[i][(j + off) % m] = a[i][j];
    }
    while (off < m && (lim > c[i][off] || (cnt && lim == c[i][off]) )) {
      if (lim == c[i][off])
        --cnt;
      ++off;
    }
  }
  for (int i = 0; i < n; ++i) {
    for (int j = 0; j < m; ++j) {
      cout << c[i][j] << ' ';
      
    }
    cout << endl;
  }
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

