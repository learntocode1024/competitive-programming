#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x)                                                              \
  if ((bool)x)                                                                 \
    printf("YES");                                                             \
  else                                                                         \
    printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 505

int rem[MX];
int a[MX][MX];

void solve() {
  int n;
  cin >> n;
  for (int i = 1; i <= n; ++i)
    rem[i] = i - 1;
  for (int i = 1; i <= n; ++i)
    cin >> a[i][i];
  for (int i = 1; i <= n; ++i) {
    int grp = a[i][i];
    int x = i, y = i;
    while (rem[grp]) {
      if (x > 1 && a[x - 1][y] == 0) {
        a[x - 1][y] = grp;
        --x;
      } else if (y < n && a[x][y + 1] == 0) {
        a[x][y + 1] = grp;
        ++y;
      } else
        break;
      --rem[grp];
    }
    if (rem[grp]) { // fail
      cout << -1 << endl;
      return;
    }
  }
  for (int i = 1; i <= n; ++i) {
    for (int j = 1; j <= i; ++j) {
      cout << a[j][i] << ' ';
    }
    cout << endl;
  }
}

int main() {
  int T = 1;
  // cin >> T;
  while (T--)
    solve();
  return 0;
}

