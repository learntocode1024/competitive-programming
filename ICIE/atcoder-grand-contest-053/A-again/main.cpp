#include <string>
#include <iostream>
#include <algorithm>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 105

int a[MX], b[MX];

void solve() {
  int n;
  string s;
  cin >> n >> s;
  for (int i = 0; i <= n; ++i)
    cin >> a[i];
  b[0] = 0;
  int minv = 0;
  for (int i = 0; i < n; ++i) {
    b[i + 1] = b[i] - s[i] + '<' + 1;
    minv = min(minv, b[i + 1]);
  }
  for (int i = 0; i <= n; ++i)
    b[i] -= minv;
  int k = INT_MAX;
  for (int i = 1; i <= n; ++i)
    k = min(k, abs(a[i] - a[i - 1]));
  for (int i = 0; i <= n; ++i)
    if (b[i]) k = min(k, a[i] / b[i]);
  cout << k << endl;
  for (int i = 1; i < k; ++i) {
    for (int i = 0; i <= n; ++i)
      cout << b[i] << ' ', a[i] -= b[i];
    cout << endl;
  }
  for (int i = 0; i <= n; ++i)
    cout << a[i] << ' ';
  cout << endl;
}

int main() {
  int T = 1;
  // cin >> T;
  while (T--) {
    solve();
  }
  return 0;
}

