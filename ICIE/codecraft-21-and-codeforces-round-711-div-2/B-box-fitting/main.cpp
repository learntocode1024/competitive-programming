int cnt[21];

void solve() {
  for (int i = 0; i < 21; ++i)
    cnt[i] = 0;
  int n, w;
  cin >> n >> w;
  for (int i = 0; i < n; ++i) {
    int t;
    cin >> t;
    int lg = -1;
    while (t)
      ++lg, t >>= 1;
    ++cnt[lg];
  }
  int ans = 0;
  while (n) {
    ++ans;
    int rem = w;
    for (int i = 20; i >= 0; --i)
      while (cnt[i] && rem >= (1 << i))
        --cnt[i], --n, rem -= (1 << i);
    // cerr << rem << endl;
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}
 
