#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 200005

int a[MX], b[MX];

void solve() {
  int n, l, r;
  cin >> n >> l >> r;
  for (int i = 1; i <= n; ++i) {
    a[i] = b[i] = 0;
  }
  for (int i = 0; i < l; ++i) {
    int tmp;
    cin >> tmp;
    ++a[tmp];
  }
  for (int i = 0; i < r; ++i) {
    int tmp;
    cin >> tmp;
    ++b[tmp];
  }
  int sin = n;
  for (int i = 1; i <= n; ++i) {
    int tmp = min(a[i], b[i]);
    a[i] -= tmp;
    b[i] -= tmp;
    sin -= 2 * tmp;
  }
  int x = 0, y = 0,x0 = 0, y0 = 0;
  for (int i = 1; i <= n; ++i) {
    x += a[i];
    x0 += a[i] & 1;
  }
  for (int i = 1; i <= n; ++i) {
    y += b[i];
    y0 += b[i] & 1;
  }
  sin /= 2;
  x -= x0;
  y -= y0;
  if (x0 > y0) {
    sin += max((x0 - y0) - y0, 0)/2;
  } else {
    sin += max((y0 - x0) - x0, 0)/2;
  }
  cout << sin << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

