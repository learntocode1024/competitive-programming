#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 105

int vis[MX];
int ans[MX];

void solve() {
  int n, x;
  cin >> n >> x;
  for (int i = 1; i < MX; ++i) vis[i] = 0;
  for (int i = 0; i < n; ++i) {
    int tmp;
    cin >> tmp;
    vis[tmp] = 1;
  }
  int cnt = 0, sum = 0, pos = 0;
  for (int i = 1; i < MX; ++i) {
    if (vis[i]) {
      ++cnt;
      ans[cnt] = i;
      sum += i;
      if (sum == x) pos = cnt;
    }
  }
  if (pos && ans[pos] == ans[n]) {
    cout << "NO" << endl;
    return;
  }
  if (pos)swap(ans[pos], ans[n]);
  cout << "YES" << endl;
  for (int i = 1; i <= n; ++i) {
    cout << ans[i] << ' ';
  }
  cout << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

