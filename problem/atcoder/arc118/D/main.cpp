#include <algorithm>
#include <cstdio>
#include <cstring>
#include <iostream>
#include <vector>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x)                                                              \
  if ((bool)x)                                                                 \
    printf("YES");                                                             \
  else                                                                         \
    printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

void solve() {
  LL p, a, b;
  cin >> p >> a >> b;
  vector<LL> pa, pb;
  bool flaga = (b == 1);
  bool flagb = (a == 1);
  pa.pb(1);
  for (LL curr = a; curr != 1; curr = curr * a % p) {
    pa.pb(curr);
    if (b == curr)
      flaga = true;
  }
  pb.pb(1);
  for (LL curr = b; curr != 1; curr = curr * b % p) {
    pb.pb(curr);
    if (a == curr)
      flagb = true;
  }
  if (pa.size() == p - 1) {
    cout << "Yes" << endl;
    for (auto i : pa) {
      cout << i << ' ';
    }
    cout << 1 << endl;
    return;
  }
  if (pb.size() == p - 1) {
    cout << "Yes" << endl;
    for (auto i : pb) {
      cout << i << ' ';
    }
    cout << 1 << endl;
    return;
  }
  if (flaga && flagb || pa.size() * pb.size() < p - 1) {
    cout << "No" << endl;
    return;
  }
  int sa = pa.size(), sb = pb.size();
  LL cur = 1;
  if (sb & 1) {
    swap(a, b);
    swap(sa, sb);
    swap(pa, pb);
  }
  for (int i = 0; i < (p - 1) / sa; ++i) {
    if (i & 1) {
      for (int j = sa - 1; j >= 0; --j) {
        cout << pa[j] * cur % p << ' ';
      }
    } else {
      for (int j = 0; j < sa; ++j) {
        cout << pa[j] * cur % p << ' ';
      }
    }
    cur = cur * b % p;
  }
  cout << 1 << endl;
}

int main() {
  int T = 1;
  /* cin >> T; */
  while (T--)
    solve();
  return 0;
}

