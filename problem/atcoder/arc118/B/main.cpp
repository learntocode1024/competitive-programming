#include <algorithm>
#include <cstdio>
#include <cmath>
#include <iostream>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x)                                                              \
  if ((bool)x)                                                                 \
    printf("YES");                                                             \
  else                                                                         \
    printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 100005

LL a[MX], b[MX];
pair<double, int> diff[MX];
void solve() {
  int k, m, n;
  cin >> k >> n >> m;
  for (int i = 0; i < k; ++i)
    cin >> a[i];
  LL sum0 = 0;
  for (int i = 0; i < k; ++i) {
    b[i] = floor(1.0l * a[i] * m / n);
    sum0 += b[i];
    diff[i].second = i;
    diff[i].first = -abs(1.0l * b[i] / m - 1.0l * a[i] / n);
  }
  sort(diff, diff + k);
  for (int i = 0; i < m - sum0; ++i) {
    ++b[diff[i].second];
  }
  for (int i = 0; i < k; ++i) {
    cout << b[i] << ' ';
  }
  /* cout << endl; */
}

int main() {
  int T = 1;
  /* cin >> T; */
  while (T--)
    solve();
  return 0;
}

