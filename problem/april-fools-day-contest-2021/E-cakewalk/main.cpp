string s[5];

void solve() {
  int n, m;
  cin >> n >> m;
  for (int i = 0; i < n; ++i)
    cin >> s[i];
  int x = 0, y = 0;
  int ans = 0;
  while (x < n - 1 && y < m - 1) {
    ans += s[x][y] == '*';
    bool found = 0;
    for (int i = 1; !found && i < n + m - 2; ++i) {
      for (int j = y + i; j >= y; --j) {
        int k = x + i - j + y;
        if (k >= n) break;
        if (s[k][j] == '*') {
          found = 1;
          x = k, y = j;
          ans++;
          break;
        }
      }
    }
    if (!found) break;
  }
  cout << (ans + (s[n - 1][m - 1] == '*')) << endl;
}

