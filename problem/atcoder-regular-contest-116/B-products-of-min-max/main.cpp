#include <cstdio>
#include <iostream>
#include <algorithm>
using namespace std;
typedef unsigned long long LL;
const LL mod = 998244353;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 200005

LL pow[MX], inv[MX];
LL a[MX], b[MX];

void solve() {
  int n;
  cin >> n;
  inv[0] = pow[0] = 1;
  pow[1] = 2, inv[1] = 499122177;
  for (int i = 2; i <= n; ++i) {
    inv[i] = inv[i - 1] * 499122177ll % mod;
    pow[i] = pow[i - 1] * 2 % mod;
  }
  for (int i = 0; i < n; ++i)
    cin >> a[i];
  sort(a, a + n);
  for (int i = 0; i < n; ++i)
    b[i] = a[i] * pow[i] % mod;
  for (int i = 1; i < n; ++i)
    b[i] += b[i - 1];
  LL ans = 0;
  for (int i = 0; i < n; ++i) {
    LL curr = a[i] * a[i] % mod;
    if (i < n - 1)
      curr = (curr + (mod + b[n - 1] - b[i]) % mod * inv[i + 1] % mod * a[i] % mod ) % mod;
    ans = (ans + curr) % mod;
  }
  cout << ans << endl;
}

int main() {
  cin.tie(NULL);
  std::ios::sync_with_stdio(false);
  int T = 1;
  // cin >> T;
  while (T--) {
    solve();
  }
  return 0;
}

