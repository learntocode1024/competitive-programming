void solve() {
  int n;
  cin >> n;
  if (n == 1) {
    cout << 1 << endl;
    return;
  }
  if (n == 2) {
    cout << -1 << endl;
    return;
  }
  if (n == 3) {
    cout << R"(
2 9 7
4 6 3
1 8 5
)" << endl;
    return;
  }
  if (n == 4) {
    cout << R"(
3 1 4 2
7 5 8 6
11 9 12 10
15 13 16 14
)" << endl;
    return;
  }
  int half = n / 2 + (n & 1);
  for (int i = 0; i < n * n; i += n) {
    for (int j = 1; j <= n / 2; ++j)
      cout << j + i << ' ' << i + j + half << ' ';
    if (n & 1)
      cout << i + half;
    cout << endl;
  }
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

