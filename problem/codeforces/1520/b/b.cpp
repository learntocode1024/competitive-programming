void solve() {
  int n;
  cin >> n;
  int tmp = n;
  int t = 0;
  while(n > 9) n /= 10, ++t;
  int ans = 9 * t + n - 1;
  int cmp = n;
  for (int i = 0; i < t; ++i) cmp = cmp * 10 + n;
  ans += tmp >= cmp;
  cout << ans << endl;
}

