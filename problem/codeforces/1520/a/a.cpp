bool done[26];
string s;

void solve() {
  int n;
  cin >> n >> s;
  for (int i = 0; i < 26; ++i)
    done[i] = 0;
  char prev = s[0];
  for (int i = 1; i < n; ++i) {
    if (done[s[i] - 'A']) {
      cout << "NO" << endl;
      return;
    }
    if (s[i] != prev)
      done[prev - 'A'] = true;
    prev = s[i];
  }
  cout << "YES" << endl;
}

