#include <cstdio>
using namespace std;

int main() {
  long long n, m, cnt = 0;
  scanf("%lld%lld", &n, &m);
  for (int i = 0; i < n; ++i) {
    getchar();
    int t = 0;
    for (int j = 0; j < m; ++j) {
      t += (getchar() == '1');
    }
    cnt += (t & 1);
  }
  printf("%lld", cnt * (n - cnt));
  return 0;
}

