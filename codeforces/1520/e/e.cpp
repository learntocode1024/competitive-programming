    printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX

vector<int> vt;
string s;

void solve() {
  int n;
  cin >> n >> s;
  vt.clear();
  for (int i = 0; i < n; ++i) {
    if (s[i] == '*')
      vt.pb(i);
  }
  n = vt.size();
  int half = n / 2;
  LL ans = 0;
  for (int i = 0; i < n; ++i) {
    ans += abs(vt[half] - vt[i]) - abs(i - half);
  }
  cout << ans << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

