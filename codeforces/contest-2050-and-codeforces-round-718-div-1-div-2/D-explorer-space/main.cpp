#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 505

LL dist[25][MX][MX];
LL a[MX][MX], b[MX][MX];

void solve() {
  int n, m, k;
  cin >> n >> m >> k;
  if (k & 1) {
    for (int i = 1; i <= n; ++i) {
      for (int j = 1; j <= m; ++j) {
        cout << -1 << ' ';
      }
      cout << endl;
    }
    return;
  }
  memset(dist, 0x7f, sizeof(dist));
  for (int i = 1; i <= n; ++i) {
    for (int j = 1; j < m; ++j) {
      cin >> a[i][j];
    }
  }
  for (int i = 1; i < n; ++i) {
    for (int j = 1; j <= m; ++j) {
      cin >> b[i][j];
    }
  }
  for (int i = 1; i <= n; ++i) {
    for (int j = 1; j <= m; ++j) {
      dist[0][i][j] = 0;
      if (j < m) dist[1][i][j] = min(dist[1][i][j], a[i][j]);
      if (j > 1) dist[1][i][j] = min(dist[1][i][j], a[i][j - 1]);
      if (i > 1) dist[1][i][j] = min(dist[1][i][j], b[i - 1][j]);
      if (i < n) dist[1][i][j] = min(dist[1][i][j], b[i][j]);
    }
  }
  for (int t = 2; t <= k / 2; ++t) {
    for (int i = 1; i <= n; ++i) {
      for (int j = 1; j <= m; ++j) {
        if (j < m) dist[t][i][j] = min(dist[t][i][j], a[i][j] + dist[t - 1][i][j + 1]);
        if (j > 1) dist[t][i][j] = min(dist[t][i][j], a[i][j - 1] + dist[t - 1][i][j - 1]);
        if (i > 1) dist[t][i][j] = min(dist[t][i][j], b[i - 1][j] + dist[t - 1][i - 1][j]);
        if (i < n) dist[t][i][j] = min(dist[t][i][j], b[i][j] + dist[t - 1][i + 1][j]);
      }
    }
  }
  for (int i = 1; i <= n; ++i) {
    for (int j = 1; j <= m; ++j) {
      cout << 2 * dist[k / 2][i][j] << ' ';
    }
    cout << endl;
  }
}

int main() {
  int T = 1;
  // cin >> T;
  while (T--)
    solve();
  return 0;
}

