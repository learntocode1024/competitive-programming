#define MX 105

int ans[MX];

void solve() {
  int n, k;
  cin >> n >> k;
  if (k * 2 >= n) {
    cout << -1 << endl;
    return;
  }
  int a = n;
  for (int i = 0; i < k; ++i) {
    ans[2 * i + 1] = a--;
  }
  a = 1;
  for (int i = 0; i < n; ++i) {
    if (ans[i]) continue;
    ans[i] = a++;
  }
  for (int i = 0; i < n; ++i) {
    cout << ans[i] << ' ';
    ans[i] = 0;
  }
  cout << endl;
}

