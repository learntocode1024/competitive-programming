LL N;

LL calc(const arr &a) {
  LL ans = 0, sum = 0, sum2 = 0, isum = 0;
  LL n = a.size();
  for (LL k = 0; k < n; ++k) {
    const LL i = a[k];
    sum += i;
    sum2 += i * i;
    isum += (n - k - 1) * i;
  }
  LL ret = (N + 1) * isum - ((sum * sum - sum2) >> 1);
  return ret;
}

void solve() {
  int n;
  cin >> n;
  N = n;
  a.clear();
  for (int i = 1; i <= n; ++i) {
    long long tmp;
    cin >> tmp;
    a[tmp].push_back(i);
  }
  LL ans = 0;
  for (auto i : a) {
    ans += calc(i.second);
  }
  cout << ans << endl;
}
