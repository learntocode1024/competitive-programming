int n;
string s;

void solvep() {
  int cnt = 0;
  for (auto i : s)
    cnt += i == '0';
  if (cnt == 1 || ~cnt & 1) {
    cout << "BOB" << endl;
  } else {
    cout << "ALICE" << endl;
  }
}

void solve() {
  cin >> n >> s;
  string rev = s;
  reverse(rev.begin(), rev.end());
  if (s == rev) {
    solvep();
    return;
  }
  if (n & 1 && s[n / 2] == '1') {
    s = s.substr(0, n / 2) + s.substr(n / 2 + 1, n);
    --n;
  }
  if (~n & 1) {
    int cnt = 0;
    for (auto i : s)
      cnt += i == '0';
    int t = 0;
    for (int i = 0; i < n / 2; ++i) {
      t += s[i] != s[n - i - 1];
    }
    if (cnt == 1 || ~t & 1) {
      cout << "BOB" << endl;
    } else {
      cout << "ALICE" << endl;
    }
  } else {}
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

