#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
using namespace std;
typedef unsigned long long LL;

bool calc(LL v) {
  LL mod = 1;
  LL sum = 0;
  for (int i = 0; i < 19; ++i) {
    sum += (v / mod )% 10ll;
    mod *= 10;
  }
  return __gcd(sum, v) > 1;
}

void solve() {
  LL n;
  cin >> n;
  for (int i = 0; i < 3; ++i) {
    if (calc(n)) break;
    ++n;
  }
  cout << n << endl;
}

int main() {
  cin.tie(NULL);
  std::ios::sync_with_stdio(false);
  int T = 1;
  cin >> T;
  while (T--) {
    solve();
  }
  return 0;
}

