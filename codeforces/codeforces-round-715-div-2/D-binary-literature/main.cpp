string s[3];

void solve() {
  int n;
  cin >> n;
  int cnt[3];
  for (int i = 0; i < 3; ++i) cin >> s[i], cnt[i] = 0;
  for (int i = 0; i < 2 * n; ++i) {
    for (int j = 0; j < 3; ++j)
      cnt[j] += s[j][i] == '0';
  }
  int br = 0;
  for (int i = 0; i < 3; ++i)
    br += (cnt[i] >= n);
  decltype(s[0].begin()) i1, e1, i2, e2;
  if (br >= 2) {
    for (int i = 0, j = 0; i < 3 && j < 2; ++i) {
      if (cnt[i] < n) continue;
      if (!j) i1 = s[i].begin(), e1 = s[i].end(), j++;
      else i2 = s[i].begin(), e2 = s[i].end(), j++;
    }
  } else {
    for (int i = 0, j = 0; i < 3 && j < 2; ++i) {
      if (cnt[i] > n) continue;
      if (!j) i1 = s[i].begin(), e1 = s[i].end(), j++;
      else i2 = s[i].begin(), e2 = s[i].end(), j++;
    }
  }
  char v = '0' + (br < 2);
  while (i1 < e1 && i2 < e2) {
    while (i1 < e1 && *i1 != v) {
      cout << *i1;
      ++i1;
    }
    while (i2 < e2 && *i2 != v) {
      cout << *i2;
      ++i2;
    }
    cout << v;
    ++i1, ++i2;
  }
  while (i1 < e1)
    cout << *i1, i1++;
  while (i2 < e2)
    cout << *i2, i2++;
  cout << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

