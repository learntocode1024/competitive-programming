#include <cstdio>
#include <cstring>
#include <iostream>
#include <algorithm>
#include <vector>
#include <map>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 200005

LL ans[MX];
map<int, vector<LL>> st;
int tpc[MX];

void solve() {
  st.clear();
  int n;
  cin >> n;
  for (int i = 0; i < n; ++i) {
    cin >> tpc[i];
  }
  LL all = 0;
  for (int i = 0; i < n; ++i) {
    LL tmp;
    cin >> tmp;
    all += tmp;
    st[tpc[i]].push_back(tmp);
  }
  int maxk = 0;
  for (auto i = st.begin(); i != st.end(); ++i) {
    maxk = max(maxk, (int) i->second.size());
    sort(i->second.begin(), i->second.end());
  }
  for (int i = 1; i <= maxk; ++i) ans[i] = all;
  for (int i = maxk + 1; i <= n; ++i)
    ans[i] = 0;
  for (auto i = st.begin(); i != st.end(); ++i) {
    auto &v = i->second;
    for (int j = 1; j < v.size(); ++j) {
      v[j] += v[j - 1];
    }
    for (int j = 1; j <= maxk; ++j) {
      if (v.size() % j)
        ans[j] -= v[v.size() % j - 1];
    }
  }
  for (int i = 1; i <= n; ++i) cout << ans[i] << ' ';
  cout << endl;
}

int main() {
  int T = 1;
  cin >> T;
  while (T--)
    solve();
  return 0;
}

