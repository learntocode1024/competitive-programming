#include <cstdio>
#include <string>
#include <iostream>
#include <algorithm>
using namespace std;
typedef long long LL;
typedef unsigned long long ULL;
const LL mod = 1e9 + 7;
#define printb(x) \
  if ((bool)x) printf("YES"); \
  else printf("NO");
#define pb(x) push_back(x)
#define pf(x) push_front(x)
#define MX 300005

string s, t;
bool a[MX], b[MX];

void solve() {
  int n;
  cin >> n >> s >> t;
  if (s == t) {
    cout << "YES" << endl;
    return;
  }
  for (int i = n - 1; i >= 0; --i)
    a[i] = s[n - 1 - i] != '0';
  for (int i = n - 1; i >= 0; --i)
    b[i] = t[n - 1 - i] != '0';
  for (int i = 0; i < n; ++i) {
    b[i] = a[i] != b[i];
  }
  int bit = b[n - 1];
  int flag = 2 * a[n - 1] - 1;
  for (int i = n - 2; i >= 0; --i) {
    if (b[i] != bit) {
      if (flag != 0) {
        cout << "NO" << endl;
        return;
      }
      bit = b[i];
    }
    flag += 2 * a[i] - 1;
  }
  if (bit && flag != 0) {
    cout << "NO" << endl;
    return;
  }
  cout << "YES" << endl;
}

int main() {
  cin.tie(NULL);
  std::ios::sync_with_stdio(false);
  int T = 1;
  cin >> T;
  while (T--) {
    solve();
  }
  return 0;
}

