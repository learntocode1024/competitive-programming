namespace IO {
char in[1 << 21]; // sizeof in varied in problem
char const *o;

void init_in() {
  o = in;
  in[fread(in, 1, sizeof(in) - 4, stdin)] = 0; // set 0 at the end of buffer.
}

int readInt() {
  unsigned u = 0, s = 0;

  while (*o && *o <= 32)
    ++o; // skip whitespaces...

  if (*o == '-')
    s = ~s, ++o;
  else if (*o == '+')
    ++o; // skip sign

  while (*o >= '0' && *o <= '9')
    u = (u << 3) + (u << 1) + (*o++ - '0'); // u * 10 = u * 8 + u * 2 :)

  return static_cast<int>((u ^ s) + !!s);
}

int readPoly(int64_t *a) {
  int64_t *a0 = a;
  unsigned u = 0, s = 0;

  while (*o && *o <= 32)
    ++o; // skip whitespaces...

  if (*o == '-')
    s = ~s, ++o;
  else if (*o == '+')
    ++o; // skip sign

  while (*o >= '0' && *o <= '9')
    *(a++) = (*o++ - '0');

  reverse(a0, a);
  return a - a0;
}

} // namespace IO

LL Q_pow(LL x, int p) {
  LL ret = 1;
  while (p) {
    if (p & 1)
      ret = ret * x % MOD;
    x = x * x % MOD;
    p >>= 1;
  }
  return ret;
}
namespace DFT {
const int max_len = 1 << 20;
int bit_inv[max_len];
LL gn[max_len];
void prework() {
  gn[1] = Q_pow(3, (MOD - 1) / max_len);

  for (int i = 2; i < max_len; ++i)
    gn[i] = gn[i - 1] * gn[1] % MOD;
}

void ntt(int64_t *a, const int len, const int opt) {
  for (int i = 1; i < len; ++i) {
    bit_inv[i] = bit_inv[i >> 1] >> 1;
    if (i & 1)
      bit_inv[i] |= len >> 1;
    if (bit_inv[i] > i)
      swap(a[i], a[bit_inv[i]]);
  }
  for (int n = 1; n < len; n <<= 1) {
    LL g0 = gn[max_len / n / 2];
    for (int i = 0; i < len; i += n * 2) {
      LL g = 1;
      for (int j = i; j < i + n; ++j) {
        LL x = a[j], y = g * a[j + n] % MOD;
        a[j] = (x + y) % MOD;
        a[j + n] = (MOD + x - y) % MOD;
        g = g * g0 % MOD;
      }
    }
  }
  if (opt == -1) {
    LL t = Q_pow(len, MOD - 2);
    for (int i = 0; i < len; ++i) {
      a[i] = a[i] * t % MOD;
    }
    reverse(a + 1, a + len);
  }
}
} // namespace DFT

namespace POLY {
int64_t X[MX], Y[MX], Z[MX];
int64_t upper_power_of_two(int64_t v) {
  v--;
  v |= v >> 1;
  v |= v >> 2;
  v |= v >> 4;
  v |= v >> 8;
  v |= v >> 16;
  v |= v >> 32;
  v++;
  return v;
}
int inv(int64_t *o, int64_t *a, int len) {
  if (!a[0])
    return 1;
  Z[0] = Q_pow(a[0], MOD - 2);
  Z[1] = 0;
  for (int n = 2; n < len * 2; n <<= 1) {
    int lim = n << 1;
    for (int i = 0; i < n; ++i)
      X[i] = Z[i], Y[i] = a[i];
    for (int i = n; i < lim; ++i)
      X[i] = Y[i] = 0;
    DFT::ntt(X, lim, 1);
    DFT::ntt(Y, lim, 1);
    for (int i = 0; i < lim; ++i) {
      X[i] = (2 * X[i] - X[i] * X[i] % MOD * Y[i] % MOD + MOD) % MOD;
    }
    DFT::ntt(X, lim, -1);
    for (int i = 0; i < n; ++i)
      Z[i] = X[i];
  }
  for (int i = 0; i < len; ++i)
    o[i] = Z[i];
  return 0;
}
int mul(int64_t *o, int64_t *a, int64_t *b, int _len) {
  int len = upper_power_of_two(_len);
  if (len != (len & -len))
    return 1;
  for (int i = 0; i < len; ++i)
    X[i] = a[i], Y[i] = b[i];
  DFT::ntt(X, len, 1);
  DFT::ntt(Y, len, 1);
  for (int i = 0; i < len; ++i)
    X[i] = X[i] * Y[i] % MOD;
  DFT::ntt(X, len, -1);
  for (int i = 0; i < _len; ++i)
    o[i] = X[i];
  return 0;
}
void input(int64_t *o, int len) {
  for (int i = 0; i < len; ++i)
    scanf("%lld", o + i);
}
int print(int64_t *o, int len) {
  for (int i = 0; i < len; ++i)
    printf("%lld ", o[i]);
  return printf("\n");
}
} // namespace POLY

int64_t a[MX] = {1, 2, 1, 0}, b[MX], c[16];

void solve() {
  IO::init_in();
  int m = IO::readPoly(a);
  int n = IO::readPoly(b);
  POLY::mul(c, a, b, m + n);
  for (int i = 0; i < m + n; ++i)
    c[i + 1] += c[i] / 10, c[i] %= 10;
  int i = 0;
  int f = 1;
  string s;
  while (f || c[i] != 0)
    f = !c[i], s.push_back(c[i++] + '0');
  reverse(begin(s), end(s));
  cout << s << endl;
}

int main() {
  DFT::prework();
  int T = 1;
  while (T--)
    solve();
  return 0;
}

